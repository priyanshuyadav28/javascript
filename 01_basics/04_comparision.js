// === strict equality operator checks the equality by checking the value as well as the data type of the two operands
let a = 23;
let b = "23";

console.log(a == b); // true because the value of a and b are same according to the == operator

console.log(a === b); // false because a is a number and b is a string so they are not equal in data type

